function updateJson(locale: string, localeTranslations: Record<string, string>) {
    const filePath = path.join(MESSAGES_DIR, `${locale}.json`);
    if (!fs.existsSync(filePath)) return;

    const messages = JSON.parse(fs.readFileSync(filePath, 'utf-8'));

    const setNestedKey = (obj: any, key: string, value: string) => {
        const parts = key.split('.');
        let current = obj;
        for (let i = 0; i < parts.length - 1; i++) {
            if (!current[parts[i]]) current[parts[i]] = {};
            current = current[parts[i]];
        }
        current[parts[parts.length - 1]] = value;
    };

    Object.entries(localeTranslations).forEach(([key, val]) => {
        setNestedKey(messages, key, val);
    });

    fs.writeFileSync(filePath, JSON.stringify(messages, null, 2));
    console.log(`âœ… Updated ${locale}.json with ${Object.keys(localeTranslations).length} translations.`);
}

